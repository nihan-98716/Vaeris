@patch("requests.post")
def test_pipeline_handles_llm_timeout_gracefully(mock_post):
    """Verifies that the main pipeline runs end-to-end even when the LLM times out."""
    mock_post.side_effect = requests.exceptions.Timeout("Connection timed out")

    with patch.dict("os.environ", {"OPENAI_API_KEY": "fake-key"}):
        res = run_investigation_pipeline(
            latitude=28.566,
            longitude=77.186,
            horizon_hours=24,
            budget=5000.0,
            inspectors=5,
            max_travel_time_hours=3.0,
            enable_llm=True,
        )

        assert res.latitude == 28.566
        assert "Environmental Investigation Report" in res.summary
        assert res.llm_error is True
